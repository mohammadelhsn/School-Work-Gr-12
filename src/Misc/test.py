add = lambda a, b: a + b
subtract = lambda a, b: a - b
multiply = lambda a, b: a * b
divide = lambda a, b: a / b
print(add(10, 5))
print(subtract(10, 5))
print(multiply(10, 5))
print(divide(10, 5))


def addv2(a, b):
    return a + b


def subtractv2(a, b):
    return a - b


def multiplyv2(a, b):
    return a * b


def dividev2(a, b):
    return a / b


print(addv2(10, 5))
print(subtractv2(10, 5))
print(multiplyv2(10, 5))
print(dividev2(10, 5))
